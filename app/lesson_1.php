                            <p style="text-indent:50px;"> We present the conic sections, a particular class of curves
                                which sometimes
                                appear in nature and which have applications in other fields. In this lesson, we first
                                illustrate how each of these curves is obtained from the intersection of a plane and a
                                cone, and then discuss the first of their kind, circles. The other conic sections will
                                be
                                covered in the next lessons.<br>
                            <p style="text-indent:50px;"><b>Conic sections</b> (or conics), is a curved formed by a
                                plane passing through a
                                double-napped circular cone. One of the first shapes we learned, a circle, is a conic.
                                When you throw a ball, the trajectory it takes is a parabola. The orbit taken by each
                                planet around the sun is an ellipse. Properties of hyperbolas have been used in the
                                design of certain telescopes and navigation systems. We will discuss circles in this
                                lesson, leaving parabolas, ellipses, and hyperbolas for subsequent lessons.<br></p>
                            <p>

                            <ul style="text-indent:50px;">
                                <li><b>Circle</b> (Figure 1.1) – is a special case of ellipse in which the plane is
                                    perpendicular to the axis of the cone.</li><br>
                                <li><b>Ellipse</b> (Figure 1.1) - when the (tilted) plane intersects only one cone to
                                    form a bounded curve</li><br>
                                <li><b>Parabola</b> (Figure 1.2) – the plane is parallel to a generator line of the cone
                                </li><br>
                                <li><b>Hyperbola</b> (Figure 1.3) – the intersection is an unbounded curve and the plane
                                    is not parallel to a generator line of the cone and the plane intersects both halves
                                    of the cone.</li><br>
                            </ul>
                            <center><img src="images/lesson3.png" width="700" height="500" /></center>

                            <p>We can draw these conic sections on a rectangular coordinate plane and find
                                their equations. To be able to do this, we will present equivalent definitions of these
                                conic sections in subsequent sections, and use these to find the equations.
                                There are other ways for a plane and the cones to intersect, to form what are
                                referred to as degenerate conics: a point, one line, and two lines. See Figures 1.4,
                                1.5, and 1.6.</p>
